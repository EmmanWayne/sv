package clases;

public class votantes {

	int id_votante;
	String nombre_votante;
	String dni_votante;
	String grado_votante;
	String modalidad_votante;
	int estado_votante;

	public int getId_votante() {
		return id_votante;
	}

	public void setId_votante(int id_votante) {
		this.id_votante = id_votante;
	}

	public String getNombre_votante() {
		return nombre_votante;
	}

	public void setNombre_votante(String nombre_votante) {
		this.nombre_votante = nombre_votante;
	}

	public String getDni_votante() {
		return dni_votante;
	}

	public void setDni_votante(String dni_votante) {
		this.dni_votante = dni_votante;
	}

	public String getGrado_votante() {
		return grado_votante;
	}

	public void setGrado_votante(String grado_votante) {
		this.grado_votante = grado_votante;
	}

	public String getModalidad_votante() {
		return modalidad_votante;
	}

	public void setModalidad_votante(String modalidad_votante) {
		this.modalidad_votante = modalidad_votante;
	}

	public int getEstado_votante() {
		return estado_votante;
	}

	public void setEstado_votante(int estado_votante) {
		this.estado_votante = estado_votante;
	}

}
