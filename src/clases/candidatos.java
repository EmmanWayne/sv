package clases;

public class candidatos {

	int id_candidato;
	String nombre_candidato;
	String foto_candidato;
	int votos_candidato;

	public int getId_candidato() {
		return id_candidato;
	}

	public void setId_candidato(int id_candidato) {
		this.id_candidato = id_candidato;
	}

	public String getNombre_candidato() {
		return nombre_candidato;
	}

	public void setNombre_candidato(String nombre_candidato) {
		this.nombre_candidato = nombre_candidato;
	}

	public String getFoto_candidato() {
		return foto_candidato;
	}

	public void setFoto_candidato(String foto_candidato) {
		this.foto_candidato = foto_candidato;
	}

	public int getVotos_candidato() {
		return votos_candidato;
	}

	public void setVotos_candidato(int votos_candidato) {
		this.votos_candidato = votos_candidato;
	}

}
